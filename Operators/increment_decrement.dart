void main() {
  int a = 5;
  int b = ++a; // a is incremented to 6, then b is assigned the value of a (6)

  print('a: $a'); // Output: a: 6
  print('b: $b'); // Output: b: 6

  int x = 5;
  int y = x++; // y is assigned the value of x (5), then x is incremented to 6

  print('x: $x'); // Output: x: 6
  print('y: $y'); // Output: y: 5
}

