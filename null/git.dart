// GitHub Command-line Commands
// To manage GitHub repositories via command line, here are key commands:
//
// git clone [repo_url]: Clones a remote repository to your local machine.
// git status: Shows the status of your current repository, indicating tracked and untracked files.
// git add [file_name]: Stages changes for commit. Use . to add all modified files.
// git commit -m "Your commit message": Commits your staged changes with a message describing them.
// git push: Pushes your local commits to the remote repository.
// git pull: Updates your local repository with the latest changes from the remote.